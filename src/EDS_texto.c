void d_letra(DIBUJO_EDS* dibujo, uint8_t letra, int32_t X, int32_t Y, COLOR color) {
	
	error_EDS_x[0] = 0;
	if(dibujo == NULL) {
		
		snprintf(error_EDS_x, sizeof(error_EDS_x), "El dibujo de origen está dañado.");
		return;
	}
	
	uint32_t x1, y1;
	for(y1 = 0; y1 < 14; y1++) {
		
		for(x1 = 0; x1 < 7; x1++) {
			
			if(fuente_std[letra][7 * y1 + x1]) d_pixel(dibujo, X + x1, Y + y1, color, false);
		}
	}
}


void d_cadena(DIBUJO_EDS* dibujo, const char* cadena, int32_t X, int32_t Y, COLOR color) {
	
	error_EDS_x[0] = 0;
	if(dibujo == NULL) {
		
		snprintf(error_EDS_x, sizeof(error_EDS_x), "El dibujo de origen está dañado.");
		return;
	}
	
	uint32_t C;
	for(C = 0; C < strlen(cadena); C++) d_letra(dibujo, cadena[C], X + 7 * C, Y, color);
}

